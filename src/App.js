import { useState } from "react";
import { useEffect } from "react";
//const key = "e8e1da1c5dc8da38bbec8ad3feac87ce"; tmdb key
const key = "2713f0c1";

const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isloading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  useEffect(function () {
    async function fetchMovies() {
      try {
        setIsLoading(true);
        const res = await fetch(
          // `https://api.themoviedb.org/3/discover/movie?api_key=${key}&`
          `http://www.omdbapi.com/?apikey=${key}&s=planet of Apes`
        );

        if (!res.ok) {
          throw new Error("smt went wrong  with fetching movies");
        }
        const data = await res.json();
        setMovies(data.Search);
        setWatched([]);
      } catch (err) {
        setError(err.message);
        console.log(err.message);
      } finally {
        setIsLoading(false);
      }
    }
    fetchMovies();
  }, []);
  return (
    <>
      <NavBar>
        <Logo />
        <Search />
        <Numresults movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {/* {isloading ? <Loader /> : <MovieList movies={movies} />} */}
          {isloading && <Loader />}
          {!isloading && !error && <MovieList movies={movies} />}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          <WatchedSummary watched={watched} />
          <WatchedMoviesList watched={watched} />
        </Box>
      </Main>
    </>
  );
}

